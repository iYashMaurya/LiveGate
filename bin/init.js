#!/usr/bin/env node
import { writeFileSync, mkdirSync, existsSync, readFileSync, copyFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';
import { createInterface } from 'readline';

const __dirname = dirname(fileURLToPath(import.meta.url));
const LIVEGATE_ROOT = resolve(__dirname, '..');

const rl = createInterface({ input: process.stdin, output: process.stdout });
const ask = (q) => new Promise(r => rl.question(q, r));

async function main() {
  const cwd = process.cwd();

  console.log(`
╔══════════════════════════════════════════╗
║          LiveGate — Project Setup        ║
╚══════════════════════════════════════════╝
`);

  // Check if already initialized
  if (existsSync(resolve(cwd, '.livegate.yaml'))) {
    console.log('⚠  .livegate.yaml already exists. Overwrite? (y/N)');
    const overwrite = await ask('> ');
    if (overwrite.toLowerCase() !== 'y') {
      console.log('Aborted.');
      process.exit(0);
    }
  }

  // Ask questions
  const stagingUrl = await ask('Staging server URL [http://localhost:3001]: ') || 'http://localhost:3001';
  const startCmd = await ask('Start command (e.g. "node server.js", leave empty if staging is external): ') || '';
  const healthEndpoint = await ask('Health check endpoint [/health]: ') || '/health';

  console.log('\nLog source options: file | otel | datadog | cloudwatch');
  const logSource = await ask('Log source [file]: ') || 'file';

  let logPath = '';
  if (logSource === 'file') {
    logPath = await ask('Path to access log [./access.log]: ') || './access.log';
  }

  rl.close();

  // Generate .livegate.yaml
  const config = `# LiveGate Configuration
# Generated by: npx livegate init
# Docs: https://github.com/iYashMaurya/LiveGate

staging:
  url: ${stagingUrl}
${startCmd ? `  start_command: "${startCmd}"` : '  # start_command: "node server.js"'}
  health_endpoint: ${healthEndpoint}
  startup_wait_seconds: 3

logs:
  source: ${logSource}
${logSource === 'file' ? `  path: ${logPath}` : `  # path: ./access.log`}
${logSource === 'otel' ? '  jaeger_url: http://localhost:16686' : ''}
${logSource === 'datadog' ? '  # Set DD_API_KEY and DD_APP_KEY as env vars' : ''}
${logSource === 'cloudwatch' ? '  # Set CW_LOG_GROUP and AWS_REGION as env vars' : ''}

check:
  diff: auto
  max_probes: 50
  baseline_path: .livegate/baseline.json

github:
  post_comment: true
  block_on_nogo: true
  warn_on_escalate: true
`;

  writeFileSync(resolve(cwd, '.livegate.yaml'), config);
  console.log('\n✓ Created .livegate.yaml');

  // Create .livegate/ directory for baseline storage
  const livegateDir = resolve(cwd, '.livegate');
  if (!existsSync(livegateDir)) {
    mkdirSync(livegateDir, { recursive: true });
    writeFileSync(resolve(livegateDir, 'baseline.json'), '{}');
    console.log('✓ Created .livegate/baseline.json');
  }

  // Copy GitHub Actions workflow
  const workflowDir = resolve(cwd, '.github/workflows');
  if (!existsSync(workflowDir)) {
    mkdirSync(workflowDir, { recursive: true });
  }

  const srcWorkflow = resolve(LIVEGATE_ROOT, '.github/workflows/livegate.yml');
  const destWorkflow = resolve(workflowDir, 'livegate.yml');
  if (existsSync(srcWorkflow)) {
    copyFileSync(srcWorkflow, destWorkflow);
    console.log('✓ Created .github/workflows/livegate.yml');
  }

  // Add .livegate/ to .gitignore
  const gitignorePath = resolve(cwd, '.gitignore');
  if (existsSync(gitignorePath)) {
    const gitignore = readFileSync(gitignorePath, 'utf-8');
    if (!gitignore.includes('.livegate/')) {
      writeFileSync(gitignorePath, gitignore.trimEnd() + '\n.livegate/\n');
      console.log('✓ Added .livegate/ to .gitignore');
    }
  }

  console.log(`
╔══════════════════════════════════════════╗
║          Setup Complete!                 ║
╠══════════════════════════════════════════╣
║                                          ║
║  Next steps:                             ║
║                                          ║
║  1. Add to GitHub secrets:               ║
║     LYZR_API_KEY=sk-...                  ║
║     LYZR_AGENT_ID=69d...                 ║
║                                          ║
║  2. Test locally:                        ║
║     npx livegate check                   ║
║                                          ║
║  3. Push code — LiveGate runs on PR      ║
║                                          ║
╚══════════════════════════════════════════╝
`);
}

main().catch(err => {
  console.error(`Error: ${err.message}`);
  process.exit(1);
});
